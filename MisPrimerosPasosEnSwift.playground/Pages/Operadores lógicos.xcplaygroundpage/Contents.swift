/*:
## ⭐️ Manejo de operadores

Los operadores lógicos nos proporcionan un resultado a partir de que se cumpla o no una cierta condición, producen un resultado booleano.
Los operadores lógicos son tres; dos de ellos son binarios, el último (negación) es binario. Se evalúan de izquierda a derecha.

 
* AND ( && ) :
Devuelve un valor lógico true(1) si ambos operandos son ciertos. En caso contrario el resultado es false(0).

*/
//: ![y](and.png)

/*:
* OR ( || ) :
Este operador binario devuelve true(1) si alguno de los operandos es cierto. En caso contrario devuelve false(0).


![o](or.png)
*/

/*:
* NEGACIÓN ( ! ) :
Este operador es denominado también negación lógica y se representa en el texto escrito por la palabra inglesa NOT (otros lenguajes utilizan directamente esta palabra para representar el operador en el código).

![no](not.png)
 ¿Cómo funcionan?\
 *Ejemplos:*
*/
var pizza : Bool = true
var refresco : Bool = false

pizza && refresco
pizza || refresco
!pizza


